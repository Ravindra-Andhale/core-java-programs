package FunctionsAndMethods;

public class EvenOdd {
    public static void evenOdd( int n){
         if (n%2==0){
            System.out.println(n+" is even number");
         }
         else {
            System.out.println(n+ " is odd number");
         }
    }
    public static void main(String[] args) {
        evenOdd(5);
        }
    
}
