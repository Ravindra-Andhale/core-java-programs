public class Variables {
    public static void main(String[] args) {

        // Variable mean from the example here 'a' is a variable and it stores the value   
        int a=10;
        System.out.println(a);
        
        float b=20.05f; // we can also write as b=20.05
        System.out.println(b);

        String name="Ravindra"; 
        System.out.println(name);

        char letter= 'a';
        System.out.println(letter);

        boolean bol=true;
        System.out.println(bol);

        a=50;                   // we can change the value of a
        System.out.println(a);

    }
    
}
